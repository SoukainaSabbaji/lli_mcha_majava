public class bibliomath    
{    
    public static void main(String[] args)     
    {    
        double x = 2, y = 4;    
          
        //le maximum de 2 nums  
        System.out.println("le maximum entre x et y est: " +Math.max(x, y));      
        //la racine carre d'un nombre   
        System.out.println("la racine carree d y est: " + Math.sqrt(y));   
        //la puissance d'un nombre     
        System.out.println("x a la puissance y est: " + Math.pow(x, y));      
        //log d'une valeur      
        System.out.println("Log de x est: " + Math.log(x));   
        System.out.println("Log de y est: " + Math.log(y));      
        //la fonction sin      
        System.out.println("le sin de x est: " +Math.sin(x));     
        //la fonction cos  
        System.out.println("le cos de x est: " +Math.cos(x));  
        //la fonction tan  
        System.out.println("le tan de x est: " +Math.tan(x));  
    }    
}